        document.addEventListener('DOMContentLoaded', function() {
            const heading = document.querySelector('.animate-words');
            if (heading) {
                const text = heading.textContent;
                // Split by word boundaries but keep the spaces
                const parts = text.split(/(\s+)/);
                let wordIndex = 0;
                
                heading.innerHTML = parts.map(part => {
                    // Only apply spans to non-whitespace parts
                    if (part.trim() === '') {
                        return part; // Return original whitespace
                    } else {
                        return `<span style="--word-index: ${wordIndex++};">${part}</span>`;
                    }
                }).join('');

                // Force reflow to ensure styles are applied
                void heading.offsetHeight;

                // Intersection Observer to trigger animation when in view
                const observer = new IntersectionObserver((entries) => {
                    entries.forEach(entry => {
                        if (entry.isIntersecting) {
                            // Add a small delay to ensure the element is in the DOM
                            setTimeout(() => {
                                entry.target.classList.add('animate-in');
                                // Unobserve after animation is triggered
                                observer.unobserve(entry.target);
                            }, 100);
                        }
                    });
                }, {
                    threshold: 0.2, // Trigger when 20% of the element is visible
                    rootMargin: '0px 0px -50px 0px' // Adjust when the animation triggers
                });

                // Start observing the heading
                observer.observe(heading);
            }
        });
