        // Add click event to all team member cards and ensure consistent images
        document.addEventListener('DOMContentLoaded', function() {
            const modal = document.getElementById('teamMemberModal');
            
            // Update all team member cards to use the same image as in the modal
            Object.keys(teamMembers).forEach(memberId => {
                const member = teamMembers[memberId];
                document.querySelectorAll(`[data-member="${memberId}"] img`).forEach(img => {
                    img.src = member.image;
                });
            });
            const closeBtn = document.querySelector('.close-modal');
            
            // Add click event to all team member cards
            document.querySelectorAll('.team-member-card').forEach(card => {
                card.addEventListener('click', function() {
                    const fullName = this.querySelector('h5').textContent.trim();
                    // Find the team member by full name
                    const member = Object.values(teamMembers).find(member => member.name === fullName);
                    
                    if (member) {
                        document.getElementById('modalMemberName').textContent = member.name;
                        document.getElementById('modalMemberTitle').textContent = member.title;
                        // Split the bio into paragraphs and wrap each in <p> tags
                        const bioParagraphs = member.bio.split('\n\n').map(para => `<p>${para}</p>`).join('');
                        document.getElementById('modalMemberBio').innerHTML = bioParagraphs;
                        document.getElementById('modalMemberImage').src = member.image;
                        
                        // Update social links
                        const socialLinks = document.querySelectorAll('.social-link');
                        socialLinks[0].href = member.linkedin;
                        socialLinks[1].href = `https://twitter.com/${member.twitter}`;
                        socialLinks[2].href = member.website;
                        
                        modal.style.display = 'flex';
                        document.body.style.overflow = 'hidden'; // Prevent scrolling when modal is open
                    }
                });
            });
            
            // Close modal when clicking the close button
            closeBtn.addEventListener('click', function() {
                modal.style.display = 'none';
                document.body.style.overflow = 'auto';
            });
            
            // Close modal when clicking outside the modal content
            window.addEventListener('click', function(event) {
                if (event.target === modal) {
                    modal.style.display = 'none';
                    document.body.style.overflow = 'auto';
                }
            });
        });
    </script>
